export function getTranslation(lang: string, key: string, params?: Record<string, string>): string {
  const selectedLang = translations[lang] ? lang : 'vi'; // vi is default fallback
  let text = translations[selectedLang]?.[key] || translations['vi']?.[key] || key;
  
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, v);
    });
  }
  return text;
}
